    {
        $where = [];
        $search = [];
        if (strlen($operate=$request->operate) > 0) {
           $where[] = ['operate',$operate];
           $search['operate'] = $operate; 
        }

        $list = OperationLog::select('*')
                            ->where($where)
                            ->orderBy('created_at','desc')
                            ->paginate(10);

        return view('admin.operation_log.list',compact('list','search'));
    }

